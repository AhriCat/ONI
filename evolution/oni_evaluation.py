# Main evaluator
# ---------------------------------------------------------------------------

class ONIEvaluator:
    """
    Multi-modal evaluation suite for ONI variants.

    Evaluates across:
    - NLP: Reasoning benchmarks, coding tasks
    - Vision: Visual QA, OCR accuracy
    - Audio: Speech recognition, TTS quality
    - Robotics: Trajectory quality, task completion
    - Integration: Cross-modal coherence

    [EDITOR] Synthetic tasks are generated by default for the evolution loop.
    Replace with real datasets by calling register_task() with your own
    EvaluationTask subclasses.
    """

    # Module weights for overall score computation
    MODULE_WEIGHTS: Dict[ModuleType, float] = {
        ModuleType.NLP:          0.30,
        ModuleType.VISION:       0.20,
        ModuleType.AUDIO:        0.15,
        ModuleType.ROBOTICS:     0.15,
        ModuleType.MEMORY:       0.10,
        ModuleType.EMOTION:      0.05,
        ModuleType.WORLD_MODEL:  0.04,
        ModuleType.METACOGNITION: 0.01,
    }

    def __init__(
        self,
        oni_model: torch.nn.Module,
        benchmark_configs: Dict = None
    ):
        self.oni_model = oni_model
        self.configs = benchmark_configs or self._default_configs()
        self.task_registry: Dict[ModuleType, List[EvaluationTask]] = {
            mt: [] for mt in ModuleType
        }

    def _default_configs(self) -> Dict:
        return {
            'nlp_tasks': 50,
            'vision_tasks': 30,
            'audio_tasks': 20,
            'robotics_tasks': 20,
            'integration_tasks': 10,
        }

    def register_task(self, task: EvaluationTask):
        """Register an evaluation task."""
        self.task_registry[task.module].append(task)

    def generate_synthetic_tasks(self, hidden_dim: int, count_per_module: int = 10):
        """
        Generate synthetic evaluation tasks.

        [EDITOR] For real evaluation, replace with actual benchmark datasets
        (HumanEval, MMLU, ARC, etc.).  These synthetic tasks validate that
        the evolution loop runs end-to-end.
        """
        torch.manual_seed(42)  # Reproducible synthetic tasks
        for module in ModuleType:
            for i in range(count_per_module):
                inp = torch.randn(1, hidden_dim)
                # Expected output: deterministic tanh transform
                expected = torch.tanh(inp * 0.5 + 0.1)
                self.register_task(SyntheticReasoningTask(
                    task_id=f"synth_{module.value}_{i}",
                    module=module,
                    input_tensor=inp,
                    expected_tensor=expected
                ))

    def evaluate_variant(
        self,
        variant_model: Optional[torch.nn.Module] = None,
        modules_to_eval: Optional[List[ModuleType]] = None
    ) -> Tuple[float, List[EvaluationLog]]:
        """
        Run full evaluation on an ONI variant.

        Returns:
            overall_score: Weighted average across all modules
            eval_logs: Per-task evaluation logs
        """
        model = variant_model or self.oni_model
        modules = modules_to_eval or list(self.task_registry.keys())
        eval_logs: List[EvaluationLog] = []
        module_scores: Dict[ModuleType, List[float]] = {mt: [] for mt in ModuleType}

        for module_type in modules:
            tasks = self.task_registry.get(module_type, [])
            for task in tasks:
                start_time = time.time()
                try:
                    task_input = task.get_input()
                    with torch.no_grad():
                        raw_output = model(task_input)
                    # Extract tensor from tuple outputs (metacognition returns 4-tuple)
                    if isinstance(raw_output, tuple):
                        output = raw_output[0]
                    else:
                        output = raw_output
                    success, score = task.evaluate(output)
                    elapsed = time.time() - start_time
                    eval_logs.append(EvaluationLog(
                        task_id=task.task_id,
                        module=module_type,
                        success=success,
                        score=score,
                        time_taken=elapsed
                    ))
                    module_scores[module_type].append(score)
                except Exception as e:
                    elapsed = time.time() - start_time
                    eval_logs.append(EvaluationLog(
                        task_id=task.task_id,
                        module=module_type,
                        success=False,
                        score=0.0,
                        time_taken=elapsed,
                        error_message=str(e)
                    ))
                    module_scores[module_type].append(0.0)

        # Compute weighted overall score
        total_weight = 0.0
        weighted_sum = 0.0
        for mt, scores in module_scores.items():
            if scores:
                avg = sum(scores) / len(scores)
                w = self.MODULE_WEIGHTS.get(mt, 0.0)
                weighted_sum += avg * w
                total_weight += w

        overall_score = weighted_sum / total_weight if total_weight > 0 else 0.0
        return overall_score, eval_logs
