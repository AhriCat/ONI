import torch
import torch.nn.functional as F
from typing import Dict, List, Tuple, Any, Optional
import logging
import time

from .improvement_proposal import EvaluationLog, ModuleType

logger = logging.getLogger(__name__)


# ---------------------------------------------------------------------------
# Task base class + synthetic implementation
# ---------------------------------------------------------------------------

class EvaluationTask:
    """Base class for evaluation tasks."""

    def __init__(self, task_id: str, module: ModuleType):
        self.task_id = task_id
        self.module = module

    def get_input(self) -> Any:
        raise NotImplementedError

    def get_expected_output(self) -> Any:
        raise NotImplementedError

    def evaluate(self, actual_output: Any) -> Tuple[bool, float]:
        """Returns (success, score 0.0-1.0)"""
        raise NotImplementedError


class SyntheticReasoningTask(EvaluationTask):
    """
    Synthetic task: cosine similarity between model output and a deterministic
    transformation of the input.  Used when real benchmarks aren't available.
    """

    def __init__(self, task_id: str, module: ModuleType,
                 input_tensor: torch.Tensor, expected_tensor: torch.Tensor):
        super().__init__(task_id, module)
        self.input_tensor = input_tensor
        self.expected_tensor = expected_tensor

    def get_input(self) -> torch.Tensor:
        return self.input_tensor

    def get_expected_output(self) -> torch.Tensor:
        return self.expected_tensor

    def evaluate(self, actual_output: Any) -> Tuple[bool, float]:
        if isinstance(actual_output, tuple):
            actual_output = actual_output[0]
        # Handle 4-tuple from metacognition forward (output, conf, conflicts, meta)
        if not isinstance(actual_output, torch.Tensor):
            return False, 0.0
        try:
            out_flat = actual_output.detach().flatten().unsqueeze(0)
            exp_flat = self.expected_tensor.flatten().unsqueeze(0)
            # Pad / truncate to same length
            min_len = min(out_flat.size(1), exp_flat.size(1))
            similarity = F.cosine_similarity(
                out_flat[:, :min_len], exp_flat[:, :min_len]
            ).item()
        except Exception:
            similarity = 0.0
        score = max(0.0, float(similarity))
        return score > 0.5, score


